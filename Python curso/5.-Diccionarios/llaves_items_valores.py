diccionario = {'a': 1, 'b': 2, 'c': 3, 'd': 4}
#keys sirve para obtener las llaves de un diccionario
#values sirve para obtener los valores de un diccionario
#items sirve para obtener los items de un diccionario

llaves = tuple(diccionario.keys())
print(llaves)

valores = tuple(diccionario.values())
print(valores)

elements = tuple(diccionario.items())
print(elements)